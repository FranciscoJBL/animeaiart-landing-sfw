export function localePath(lang: Lang, page: PageKey): string {
  const slug = pageSlugs[lang][page];
  const prefix = lang === "en" ? "" : `/${lang}`;
  return `${prefix}${slug ? `/${slug}` : ""}/`;
}

export function pageForPath(rawSlug: string | undefined): { lang: Lang; page: PageKey } | null {
  const parts = (rawSlug || "").split("/").filter(Boolean);
  const maybeLang = parts[0] as Lang | undefined;
  const lang: Lang = maybeLang && languages.includes(maybeLang) ? maybeLang : "en";
  const slug = lang === "en" ? parts.join("/") : parts.slice(1).join("/");
  const entries = Object.entries(pageSlugs[lang]) as Array<[PageKey, string]>;
  const page = entries.find(([, value]) => value === slug)?.[0];
  if (!page) return null;
  return { lang, page };
}

